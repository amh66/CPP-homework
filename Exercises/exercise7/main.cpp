#include <iostream>
//The four basic operations : * / + -
using namespace std;

int main()
{
    float x,y;
    cout << "Please enter the numbers = " << endl;
    cout<<"x = ";
    cin>>x;
    cout<<"y = ";
    cin>>y;
    cout<<"\n";
    cout<<x<<" + "<<y<<" = "<<x+y<< endl;
    cout<<x<<" - "<<y<<" = "<<x-y<< endl;
    cout<<x<<" * "<<y<<" = "<<x*y<< endl;
    cout<<x<<" / "<<y<<" = "<<x/y<< endl;
    return 0;
}
